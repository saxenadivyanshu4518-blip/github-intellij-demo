package Array;

public class LengthOperator_withOuput {
    static void main(String[] args) {

        int [] arr = {56,32,48,84,43,12,45,80,73,24,17,19};

        int n = arr.length; // length Operator

        for(int i = 0 ; i < n ; i++){
            System.out.print(arr[i] + " ");
        }
    }
}
